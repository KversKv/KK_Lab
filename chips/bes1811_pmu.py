def align_to_step(voltage: float, step: float, *, round_nearest: bool = True) -> float:
    """把电压对齐到 step 的整数倍。

    round_nearest=True  → 取最近邻 step 档位 (UI 临近吸附)。
    round_nearest=False → 不裁剪 (用于 UI 显式设置时吸附)。
    """
    if step is None or step <= 0:
        return voltage
    n = round(voltage / step)
    return round(n * step, 6)


def snap_range_to_step(v_min: float, v_max: float, step: float) -> tuple[float, float]:
    """把 [v_min, v_max] 对齐到 step 的整数倍, 向外取整 (min 向下, max 向上)。"""
    if step is None or step <= 0:
        return v_min, v_max
    n_min = int(math.floor(v_min / step + 1e-9))
    n_max = int(math.ceil(v_max / step - 1e-9))
    return round(n_min * step, 6), round(n_max * step, 6)


def vbit_to_voltage(ldo_id: str, vbit: int) -> Optional[float]:
    """vbit → 输出电压 (V); 无效 vbit 返回 None。

    同时支持 LDO 与 BUCK (查表统一由 ``get_voltage_table`` 路由)。
    """
    table = get_voltage_table(ldo_id)
    if not table or vbit < 0 or vbit >= len(table):
        return None
    return table[vbit]


def voltage_to_vbit(ldo_id: str, voltage: float) -> Optional[int]:
    """输出电压 (V) → 最接近的有效 vbit; 无表返回 None。

    遇到 None 槽位自动跳过, 取电压差最小的有效 vbit。
    同时支持 LDO 与 BUCK。
    """
    table = get_voltage_table(ldo_id)
    if not table:
        return None
    best_vbit = None
    best_diff = float("inf")
    for vbit, v in enumerate(table):
        if v is None:
            continue
        diff = abs(v - voltage)
        if diff < best_diff:
            best_diff = diff
            best_vbit = vbit
    return best_vbit


def get_voltage_steps(ldo_id: str) -> list[float]:
    """返回该模块所有有效档位电压 (V), 按 vbit 升序; 无表返回空列表。"""
    table = get_voltage_table(ldo_id)
    if not table:
        return []
    return [v for v in table if v is not None]


def snap_voltage(ldo_id: str, voltage: float) -> Optional[float]:
    """把任意电压吸附到最近的真实档位电压; 无表返回 None。"""
    vbit = voltage_to_vbit(ldo_id, voltage)
    if vbit is None:
        return None
    return vbit_to_voltage(ldo_id, vbit)


