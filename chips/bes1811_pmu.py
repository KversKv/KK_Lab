# ---------------------------------------------------------------------------
# 电压 ↔ vbit 转换工具
# ---------------------------------------------------------------------------
def get_voltage_table(ldo_id: str) -> list[Optional[float]]:
    """返回指定 LDO 的电压查找表 (按 vbit 索引)。"""
    return LDO_VOLTAGE_TABLES.get(ldo_id, [])


def vbit_to_voltage(ldo_id: str, vbit: int) -> Optional[float]:
    """vbit → 输出电压 (V); 无效 vbit 返回 None。"""
    table = LDO_VOLTAGE_TABLES.get(ldo_id)
    if not table or vbit < 0 or vbit >= len(table):
        return None
    return table[vbit]


def voltage_to_vbit(ldo_id: str, voltage: float) -> Optional[int]:
    """输出电压 (V) → 最接近的有效 vbit; 无表返回 None。

    遇到 None 槽位自动跳过, 取电压差最小的有效 vbit。
    """
    table = LDO_VOLTAGE_TABLES.get(ldo_id)
    if not table:
        return None
    best_vbit = None
    best_diff = float("inf")
    for vbit, v in enumerate(table):
        if v is None:
            continue
        diff = abs(v - voltage)
        if diff < best_diff:
            best_diff = diff
            best_vbit = vbit
    return best_vbit


def get_voltage_range(ldo_id: str) -> tuple[Optional[float], Optional[float]]:
    """返回 (最低电压, 最高电压); 无表返回 (None, None)。"""
    table = LDO_VOLTAGE_TABLES.get(ldo_id)
    if not table:
        return None, None
    valid = [v for v in table if v is not None]
    if not valid:
        return None, None
    return min(valid), max(valid)


def get_reg_map(ldo_id: str) -> Optional[LdoRegMap]:
    """返回指定 LDO 的寄存器映射; 无映射返回 None。"""
    return LDO_REG_MAPS.get(ldo_id)


def is_ldo_controllable(ldo_id: str) -> bool:
    """判断该 LDO 是否支持 I2C 寄存器控制。"""
    return ldo_id in LDO_REG_MAPS
