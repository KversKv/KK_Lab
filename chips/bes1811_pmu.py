"""BES1811 PMU 芯片寄存器映射与 LDO 电压查找表。

数据来源:
  - userdata/1811 pmu inf reg.xlsx        → 寄存器位域定义
  - userdata/BES1811 LDO输出电压范围.xlsx  → vbit ↔ 输出电压对应关系

设备参数: I2C 地址 0x17, 10 位寄存器地址, 16 位数据宽度。
"""

from dataclasses import dataclass
from typing import Optional


# ---------------------------------------------------------------------------
# 位域定义
# ---------------------------------------------------------------------------
@dataclass(frozen=True)
class BitField:
    """寄存器位域: 指定寄存器地址内的 [high:low] 位区间。"""
    reg_addr: int
    high_bit: int
    low_bit: int
    default: int = 0

    @property
    def width(self) -> int:
        return self.high_bit - self.low_bit + 1

    @property
    def mask(self) -> int:
        return ((1 << self.width) - 1) << self.low_bit


@dataclass(frozen=True)
class LdoRegMap:
    """单个 LDO 的寄存器位域映射。"""
    ldo_id: str
    pu: BitField              # reg_pu_ldo_XX — 使能
    pu_dr: BitField           # reg_pu_ldo_XX_dr — 使能驱动位
    lp: BitField              # reg_lp_en_ldo_XX — LP 模式
    lp_dr: BitField           # reg_lp_en_ldo_XX_dr — LP 驱动位
    vbit_normal: BitField     # 唤醒模式电压控制字
    vbit_dsleep: BitField     # 睡眠模式电压控制字
    vbit_rc: BitField         # RC 模式电压控制字
    res_sel_dr: BitField      # 电压调整生效位 (=1 生效)


# ---------------------------------------------------------------------------
# LDO 寄存器映射表 (从 1811 pmu inf reg.xlsx 提取)
# ---------------------------------------------------------------------------
def _bf(reg, hi, lo, dflt=0):
    return BitField(reg_addr=reg, high_bit=hi, low_bit=lo, default=dflt)


